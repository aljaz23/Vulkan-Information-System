    public void napolniSezClanov(){
        listClanov.clear();
        ClanDaoImpl clanDao = new ClanDaoImpl();
        sezClanov = clanDao.getAllClan();
        for (Clan cl : sezClanov) {
            Cin cn = new Cin();
            CinDaoImpl cinDao = new CinDaoImpl();
            cn = cinDao.getCinById(cl.getIdCina());
            Status stat = new Status();
            StatusDaoImpl statDao = new StatusDaoImpl();
            stat = statDao.getStatusById(cl.getIdStatus());
            listClanov.addElement(cl.getId()+": "+ cl.getImePriimek()+", "+cl.getStarost()+", "+ cl.getUlica()+", "+cl.getDatumRoj()+", "+cn.getNaziv()+", "+stat.getNaziv());
        }
    }
    public void napolniSezStatusa(int idStatus){
        ChcStatus.removeAllItems();
        StatusDaoImpl statDao = new StatusDaoImpl();
        sezStatusov = statDao.getAllStatus();
        for (Status stat : sezStatusov) {
            ChcStatus.addItem(stat.getId()+ ", " + stat.getNaziv());
        }
        ChcStatus.setSelectedIndex(idStatus - 1 );
    
    }
    public void napolniSezCinov(int idCin){
        ChcCin.removeAllItems();
        CinDaoImpl cinDao = new CinDaoImpl();
        sezCinov = cinDao.getAllCin();
        for (Cin cin: sezCinov) {
            ChcCin.addItem(cin.getId() + ", " + cin.getNaziv());
        }
        ChcStatus.setSelectedIndex(idCin -1);
    
    }

