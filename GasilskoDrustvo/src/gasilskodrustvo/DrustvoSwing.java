    private void btnShraniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShraniActionPerformed
         ClanDaoImpl ClanDao = new ClanDaoImpl();

        if (txtID.getText().equals("")) {
            Clan cl = new Clan(
                txtImePriimek.getText(),
                Integer.parseInt(txtStarost.getText()),
                txtUlica.getText(),
                txtDatum.getText(),
                txtTel.getText(),
                txtClanarina.getText(),
                txtOdlike.getText(),
                txtSpec.getText(),
                ChcCin.getSelectedIndex()+1,
                ChcStatus.getSelectedIndex()+1);
                               
            ClanDao.insertClan(cl);
           
        } else {
            Clan clan = new Clan(
                Integer.parseInt(txtID.getText()),
                txtImePriimek.getText(),
                Integer.parseInt(txtStarost.getText()),
                txtUlica.getText(),
                txtDatum.getText(),
                txtTel.getText(),
                txtClanarina.getText(),
                txtOdlike.getText(),
                txtSpec.getText(),
                ChcCin.getSelectedIndex()+1,
                ChcStatus.getSelectedIndex()+1);

            ClanDao.updateClan(clan);
        }
        javax.swing.JOptionPane.showMessageDialog(this, "Podatki uspešno shranjeni");
        napolniSezClanov();
    }//GEN-LAST:event_btnShraniActionPerformed

    private void lstPregledValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPregledValueChanged
        ClanDaoImpl clanDao = new ClanDaoImpl();

        sezClanov = clanDao.getAllClan();

        String izbZapis = lstPregled.getSelectedValue();

        if (izbZapis != null && !izbZapis.equals("")) {

            int id = Integer.parseInt(izbZapis.substring(0, izbZapis.indexOf(":")));
            Clan cl = clanDao.getClanById(id);

            txtID.setText(String.valueOf(cl.getId()));
            txtImePriimek.setText(cl.getImePriimek());
            txtStarost.setText(String.valueOf(cl.getStarost()));
            txtUlica.setText(cl.getUlica());
            txtDatum.setText(cl.getDatumRoj());
            txtTel.setText(cl.getTelSt());
            txtClanarina.setText(cl.getClanarina());
            txtOdlike.setText(cl.getPrizOdlike());
            txtSpec.setText(cl.getSpecialnost());
            napolniSezStatusa(cl.getIdStatus());
            napolniSezCinov(cl.getIdCina());

        }
    }//GEN-LAST:event_lstPregledValueChanged

